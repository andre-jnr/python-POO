'''
    Crie uma classe Calculadora com métodos estáticos
    para realizar operações matemáticas simples
    (soma, subtração, multiplicação, divisão).
'''
class Calculadora:
    @staticmethod
    def soma(a, b):
        return a + b

    @staticmethod
    def subtracao(a, b):
        return a - b

    @staticmethod
    def multiplicacao(a, b):
        return a * b

    @staticmethod
    def divisao(a, b):
        return a / b
